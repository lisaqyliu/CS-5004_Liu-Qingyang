/**
 * Enumeration representing the players in a Tic Tac Toe game.
 */
public enum Player {
  /**
   * Represents player X.
   */
  X,

  /**
   * Represents player O.
   */
  O,

  /**
   * Represents no player, which can be used for indicating a tie or when no winner has been determined.
   */
  NOBODY
}